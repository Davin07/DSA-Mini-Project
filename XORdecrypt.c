/*The function accepts the string to be Encrypted
   and a single character key*/
void XORdecrypt(char str[],char key)
{
    int len=strlen(str);
    //loop to traverse each character
    for (int i = 0; i < len; i++) 
    {
        //perform XOR operation to each character and print
        str[i] = str[i] ^ key; 
        printf("%c",str[i]); 
    } 
} 
